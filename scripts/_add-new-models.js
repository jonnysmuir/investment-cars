console.log('=== Add New Models & Merge Hero Specs ===\n');

// Load models.json
const modelsData = JSON.parse(fs.readFileSync(MODELS_PATH, 'utf8'));
const existingSlugs = new Set(modelsData.models.map(m => m.slug));

// 1. Merge hero specs from generator extraction (if available)
if (fs.existsSync(HERO_SPECS_PATH)) {
  const heroSpecs = JSON.parse(fs.readFileSync(HERO_SPECS_PATH, 'utf8'));
  let merged = 0;
  for (const model of modelsData.models) {
    const specs = heroSpecs[model.slug];
    if (specs) {
      if (!model.heroYears && specs.heroYears) model.heroYears = specs.heroYears;
      if (!model.heroEngine && specs.heroEngine) model.heroEngine = specs.heroEngine;
      if (!model.heroBhp && specs.heroBhp) model.heroBhp = specs.heroBhp;
      merged++;
    }
  }
  console.log(`  Merged hero specs for ${merged} existing models`);
} else {
  console.log('  No _hero-specs.json found, skipping hero spec merge');
}

// 2. Add new models
let added = 0;
for (const model of NEW_MODELS) {
  if (existingSlugs.has(model.slug)) {
    console.log(`  SKIP  ${model.slug} (already exists)`);
    continue;
  }

  // Build the entry
  const entry = {
    slug: model.slug,
    make: model.make,
    model: model.model,
  };
  if (model.matchModel) entry.matchModel = model.matchModel;
  if (model.excludePatterns) entry.excludePatterns = model.excludePatterns;
  entry.heroImage = model.heroImage || '';
  entry.heroCredit = model.heroCredit || '';
  entry.description = model.description || '';
  if (model.heroYears) entry.heroYears = model.heroYears;
  if (model.heroEngine) entry.heroEngine = model.heroEngine;
  if (model.heroBhp) entry.heroBhp = model.heroBhp;
  entry.sources = model.sources || {};

  modelsData.models.push(entry);
  added++;
  console.log(`  ADD   ${model.slug}`);
}

console.log(`\n  Added ${added} new models`);

// 3. Sort: by make (alpha), then model (alpha)
modelsData.models.sort((a, b) => {
  const makeComp = a.make.localeCompare(b.make);
  if (makeComp !== 0) return makeComp;
  return a.model.localeCompare(b.model);
});

// 4. Write
fs.writeFileSync(MODELS_PATH, JSON.stringify(modelsData, null, 2) + '\n', 'utf8');
console.log(`\n  Total models: ${modelsData.models.length}`);
console.log('  Models sorted by make → model');
console.log('\nDone!');
